#include <string.h>

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (s != NULL && f != NULL)
	{
		while (s[i])
			i++;
		while (j < i)
		{
			f(j, s);
			s++;
			j++;
		}
	}
}
